class disp2valueofdatatype
{
public static void main(String []args)
{
	short s1,s2;
	byte b1,b2;
	int i1,i2;
	float f1,f2;
	double d1,d2;
	s1=1;
	s2=2;
	b1=10;
	b2=11;
	i1=123;
	i2=321;
	f1=45;
	f2=54;
	d1=20.4;
	d2=40.2;
	System.out.println("Value of Short data type="+s1+"&"+s2);
	System.out.println("Value of Byte data type="+b1+"&"+b2);
	System.out.println("Value of Integer data type="+i1+"&"+i2);
	System.out.println("Value of Float data type="+f1+"&"+f2);
	System.out.println("Value of Double data type="+d1+"&"+d2);
}
}